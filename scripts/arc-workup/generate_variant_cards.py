#!/usr/bin/env python3
"""Generate per-variant run cards for every eval variant.

Reads results.json for each eval, generates a runcard-config.tex per variant,
builds the PDF, and collects all PDFs into data/run-cards/variants/.

Usage:
    python generate_variant_cards.py [--jobs N]
"""

from __future__ import annotations

import json
import shutil
import subprocess
import sys
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path
from typing import Any

